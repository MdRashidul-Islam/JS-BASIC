const weekDay = `monday`;
switch (weekDay) {
  case `saturday`:
    console.log(`Saturday ! Today is closed`);
    break;
  case `sunday`:
    console.log(`Sunday ! Today is closed`);
    break;
  case `monday`:
    console.log(`monday ! Today is closed`);
    break;
  case `tuesday`:
    console.log(`tuesday ! Today is closed`);
    break;
  case `wednesday`:
    console.log(`wednesday ! Today is closed`);
    break;
  case `thursday`:
    console.log(`thursday ! Today is closed`);
    break;
  case `friday`:
    console.log(`friday ! Today is closed`);
    break;
  default:
    console.log(`Not a day`);
}
